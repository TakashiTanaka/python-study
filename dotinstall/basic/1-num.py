# 数値の扱い

# アンダースコアを区切り文字として使用できる
print(100_000_000)
print(1.52)
print(-3.2)

print(1.2e4) #1.2に10の4乗をかけたもの
print(1.2e-4) #1.2に10の-4乗をかけたもの

# 数値の演算

print(10 + 3)
print(10 - 3)
print(10 * 3)
print(10 ** 3)
print(10 / 3)
print(10 // 3) # 商
print(10 % 3) # 剰余
print(10 + 2 * 3)
print((10 + 2) * 3) # 計算の優先順位の確認
